
package in.oop.concept;

public class Student {
	private String name;
	private String address;
	public  Student () {
		
	}
	public Student( String c , String a) {
	name  = c;
	address = a;
	
	}
	public String getName() {
		return name;
	}

	
	public String getAddress() {
		return address;
	}
	                                                                                                                        
	
	

}
