package com.raysstec.basic;

public class Switchvowel {
	public static void main(String[] args) {
		
		char c = 'p';
		switch(c) {
		case'a':
			System.out.println("vowel");
			break;
		case'e':
			System.out.println("vowel");
			break;
		case'i':
			System.out.println("vowel");
			break;
		case 'o':
			System.out.println("vowel");
			default:
			System.out.println("not vowel");
	
		}
		 
		 
		
	}

}
                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                            